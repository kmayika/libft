#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	x;
	int	i;

	x = 0;
	i = ft_strlen(dest);
	while (src[x] != '\0')
	{
		dest[i] = src[x];
		i++;
		x++;
	}
	dest[i] = '\0';
	return (dest);
}
